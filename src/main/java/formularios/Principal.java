    @Override
    public void actionPerformed(ActionEvent e) {
        
        Object objeto = e.getSource();//recogemos tipo de objeto de e
        
        if (objeto instanceof JButton) {
            
            JButton boton = (JButton) objeto;//casting a boton
            
            switch(boton.getText()){
                
                case "Acceder modo administrador":
                    InicioAdmin admin = new InicioAdmin();
                    setVisible(false);//deshabitamos vista de inicio sesion
                    admin.setVisible(true);//mostramos el inicio de admin
                    break;
                    
                case "Registrasrse":
                    
                    Registro vistaRegistro = new Registro();
                    setVisible(false);//deshabitamos vista de inicio sesion
                     vistaRegistro.setVisible(true);//mostramos el registro
                    break;
                    
                case "Iniciar sesion":
                    break;
            }
        }
    }

